    public void show_result(){
        ArrayList<Bao_cao> temp = bcda.getBc();
        for(Bao_cao i : temp){
            model.addRow(new Object[]{
                i.getNam(),
                i.getQuy(),
                i.getCpnhap(),
                i.getCpban(),
            });
        }
    }
    public void reset_text(){
        jtxquy.setText("");
        jtxnam.setText("");
        jtxtn.setText("");
        jtxtb.setText("");
    }
    
    public void show_result_add(){
        Bao_cao temp = bcda.getBc().get(bcda.getBc().size()-1);
            model.addRow(new Object[]{
                temp.getNam(),
                temp.getQuy(),
                temp.getCpnhap(),
                temp.getCpban(),
            });
    }
    
    public  int findRowIndex(Bao_cao temp) {
        for (int i = 0; i < model.getRowCount(); i++) {
            if (model.getValueAt(i, 0).equals(temp.getNam())) {
                return i;
            }
        }
        return -1;
    }
