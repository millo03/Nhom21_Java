    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {
        //GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        try{
            int row = tblTable.getSelectedRow();
            Bao_cao temp = new Bao_cao();
            temp.setNam(jtxnam.getText());
            temp.setQuy(jtxquy.getText());
            int nhap = Integer.valueOf(jtxtn.getText());
            temp.setCpnhap(nhap);
            int ban = Integer.valueOf(jtxtb.getText());
            temp.setCpban(ban);

            tblTable.setValueAt(temp.getNam(), row, 0);
            tblTable.setValueAt(temp.getQuy(), row, 1);
            tblTable.setValueAt(temp.getCpnhap(), row, 2);
            tblTable.setValueAt(temp.getCpban(), row, 3);
            
            new DialogHelper().infoDialogMess("Cập nhật thành công");
        }catch(Exception e){
            e.getStackTrace();
            new DialogHelper().infoDialogMess("Thao tác lỗi", "Lỗi");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

