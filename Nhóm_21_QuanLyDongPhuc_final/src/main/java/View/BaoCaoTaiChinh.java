    private void tblTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblTableMouseClicked
        // TODO add your handling code here:
        if (evt.getClickCount() == 1) { // Kiểm tra sự kiện đúp chuột
            int row = tblTable.getSelectedRow(); // Lấy chỉ mục hàng của dòng được chọn
            if (row != -1) { // Kiểm tra xem có dòng nào được chọn hay không
                // Lấy dữ liệu từ mỗi cột của dòng
                String nam = tblTable.getValueAt(row, 0).toString();
                String quy = tblTable.getValueAt(row, 1).toString();
                String cpnhap = tblTable.getValueAt(row, 2).toString();
                String cpban = tblTable.getValueAt(row, 3).toString();

                jtxnam.setText(nam);
                jtxquy.setText(quy);
                jtxtn.setText(cpnhap);
                jtxtb.setText(cpban);
                int cpnhapx = Integer.valueOf(cpnhap);
                int cpbanx = Integer.valueOf(cpban);
                jlln.setText((cpbanx-cpnhapx+""));
            }
        }
    }//GEN-LAST:event_tblTableMouseClicked

