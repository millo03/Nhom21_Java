    private void tblTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblTableMouseClicked
        // TODO add your handling code here:
        if (evt.getClickCount() == 1) { // Kiểm tra sự kiện đúp chuột
            int row = tblTable.getSelectedRow(); // Lấy chỉ mục hàng của dòng được chọn
            if (row != -1) { // Kiểm tra xem có dòng nào được chọn hay không
                // Lấy dữ liệu từ mỗi cột của dòng
                String masv = tblTable.getValueAt(row, 0).toString();
                String masp = tblTable.getValueAt(row, 1).toString();
                String soluong = tblTable.getValueAt(row, 2).toString();
                String ngaydat = tblTable.getValueAt(row, 3).toString();
                String trangthai = tblTable.getValueAt(row, 4).toString();
                
                DanhSachDatHang temp = new DanhSachDatHang();
                temp = timkiem(masp, masv, ngaydat, trangthai);
                jtxmsv.setText(masv);
                jtxgc.setText(temp.getGhichu());
                jtxmsp.setText(masp);
                jtxsl.setText(soluong);
                jtxnd.setText(ngaydat);
                jtxnh.setText(temp.getNgayhen());
                if(trangthai.equals("Đã thanh toán")){
                    jrbtdtt.setSelected(true);
                    jtxtt.setText(temp.getThanhtoan()+"");
                }
                else{
                    jrbtctt.setSelected(true);
                    jtxtt.setText("");
                }
                jltt.setText(temp.getThanhtien()+"");
                jld.setText(temp.getTratien()+"");
                jldg.setText(temp.getDpda().getTtdp(masp).getGia()+"");
            }
        }
    }//GEN-LAST:event_tblTableMouseClicked

