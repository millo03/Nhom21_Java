    public void set_cell(JTable temp, int align){
        DefaultTableCellRenderer renderer = new DefaultTableCellRenderer();
        renderer.setHorizontalAlignment(align);
        TableModel tableModel = tblTable.getModel();
        for (int columnIndex = 0; columnIndex < tableModel.getColumnCount(); columnIndex++) {
            tblTable.getColumnModel().getColumn(columnIndex).setCellRenderer(renderer);
        }
    }
    
    DefaultTableCellRenderer cellRenderer = new DefaultTableCellRenderer() {
        @Override
        public Component getTableCellRendererComponent(JTable table, Object value, boolean isSelected,
                                                   boolean hasFocus, int row, int column) {
            Component component = super.getTableCellRendererComponent(table, value, isSelected, hasFocus, row, column);
            // Tô màu nền của từng hàng
            if (row % 2 == 0) {
                component.setBackground(Color.lightGray);
            } else {
                component.setBackground(Color.white);
            }
            return component;
            }
        };
        
    public  int findRowIndex(DanhSachDatHang dh) {
        for (int i = 0; i < model.getRowCount(); i++) {
            if (model.getValueAt(i, 0).equals(dh.getMasv())
                && model.getValueAt(i, 1).equals(dh.getMasp())
                && model.getValueAt(i, 2).equals(dh.getSoluong())
                && model.getValueAt(i, 3).equals(dh.getNgaydat())
                && model.getValueAt(i, 4).equals(dh.getTrangthai())){
                return i;
            }
        }
        return -1;
    }

    public void to_mau_hang(int rowcount){
        for (int i = 0; i < rowcount; i++) {
            tblTable.getColumnModel().getColumn(i).setCellRenderer(cellRenderer);
        }
    }

    public DanhSachDatHang timkiem(String msp, String msv, String ngaydat, String trangthai){
        for(DanhSachDatHang i : dsdh.getDh()){
            if(i.getMasp().equals(msp) && i.getMasv().equals(msv)
                && i.getNgaydat().equals(ngaydat) && i.getTrangthai().equals(trangthai)){
                return i;
            }
        }
        return null;
    }
    
